package assign4second;
public class Construc {
	public Construc(){
		
	}
	public double StandardMaterials(double feet){
		return feet*1200;
	}
	public double AboveStandardMaterials(double feet){
		return feet*1500;
	}
	public double HighStandardMaterials(double feet){
		return feet*1800;
	}
	public double FullyStandardMaterials(double feet){
		return feet*2500;
	}

}